     */
    public function index()
    {
        //get workers from users depend on role
        $workers =  User::whereHas('roles',function($q){
            $q->where('name', '=', 'labor')
            ->orWhere('name', '=', 'technician')
            ->orWhere('name', '=', 'vendor');
        })->get();

        return view('users.workers.index', compact('workers'));
    }

